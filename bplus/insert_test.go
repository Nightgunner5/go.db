func BenchmarkSequentialInsertion(b *testing.B) {
	b.StopTimer()

	nodes, _ := ioutil.TempFile(os.TempDir(), "bplus_seqinsert_bench")
	defer os.Remove(nodes.Name())

	values, _ := ioutil.TempFile(os.TempDir(), "bplus_seqinsert_bench")
	defer os.Remove(values.Name())

	var val BPlusValue

	val = append(val, []byte("Test123")...)

	b.SetBytes(NodeSize)
	b.StartTimer()

	for i := 0; i < b.N; i++ {
		Insert(nodes, values, BPlusKey(i), val)
	}
}

func BenchmarkRandomInsertion(b *testing.B) {
	b.StopTimer()

	nodes, _ := ioutil.TempFile(os.TempDir(), "bplus_rndinsert_bench")
	defer os.Remove(nodes.Name())

	values, _ := ioutil.TempFile(os.TempDir(), "bplus_rndinsert_bench")
	defer os.Remove(values.Name())

	var val BPlusValue

	val = append(val, []byte("Test123")...)

	var locations = make([]BPlusKey, b.N)
	for i := 0; i < b.N; i++ {
		locations[i] = BPlusKey(i)
	}

	for i := 0; i < b.N; i++ {
		j := rand.Intn(b.N)

		locations[i], locations[j] = locations[j], locations[i]
	}

	b.SetBytes(NodeSize)
	b.StartTimer()

	for i := 0; i < b.N; i++ {
		Insert(nodes, values, locations[i], val)
	}
}
